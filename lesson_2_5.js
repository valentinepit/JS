//Реализовать четыре основные арифметические операции в виде функций с двумя параметрами. Обязательно использовать оператор return.

var a = 4, b = 2;

function sum(a, b) {
    return a + b;
}

function mult(a, b) {
    return a * b;
}

function diff(a, b) {
    return a - b;
}

function dev(a, b) {
    return a / b;
}

console.log(sum(a, b), mult(a, b), diff(a, b), dev(a, b))